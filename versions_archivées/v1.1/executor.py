def investir(pool_name):
    """
    Simule un investissement dans une pool.
    """
    print(f"💸 Action simulée : INVESTIR dans la pool {pool_name}")

def desinvestir(pool_name):
    """
    Simule un retrait de fonds depuis une pool.
    """
    print(f"🏃 Action simulée : DÉSINVESTIR de la pool {pool_name}")
